#!/usr/bin/env python
"""Sparse vs dense pair-feature benchmark.

For each local structure, stores the Ca-Ca distance matrix two ways and compares
disk footprint, read time, and reconstruction fidelity:

    dense  : pairs/distance_matrix        [N,N,1] float32   (full N^2)
    sparse : pairs_sparse/distance_matrix COO radius graph  (pairs within cutoff)

Real proteins have local contact structure, so a radius graph keeps only a small
fraction of pairs - the win grows with N (dense is O(N^2), sparse ~O(N*k)).

Usage:  python benchmarks/sparse_pairs_benchmark.py [--cutoff 15.0] [--rounds 7]
"""
from __future__ import annotations

import argparse
import json
import statistics
import tempfile
import time
from datetime import datetime, timezone
from pathlib import Path

import numpy as np

REPO_ROOT = Path(__file__).resolve().parent.parent
RESULTS_DIR = Path(__file__).resolve().parent / "results"
STRUCTS = ["1UBQ", "6LU7", "4HHB", "6M0J", "6VXX", "6OHW"]


def _median_ms(fn, rounds: int) -> float:
    s = []
    for _ in range(rounds):
        t = time.perf_counter(); fn(); s.append((time.perf_counter() - t) * 1e3)
    return round(statistics.median(s), 3)


def _dir_bytes(d: Path) -> int:
    return sum(f.stat().st_size for f in d.rglob("*") if f.is_file())


def bench(pid: str, cutoff: float, rounds: int, pt) -> dict | None:
    from proteintensor.converters.mmcif import from_mmcif
    from proteintensor.pairs import compute_distance_matrix

    cif = REPO_ROOT / f"{pid}.cif"
    if not cif.exists():
        print(f"  ! {pid}.cif missing; skip"); return None

    tmp = Path(tempfile.mkdtemp())
    ptt = tmp / f"{pid}.ptt"
    data = from_mmcif(cif)
    n = int(data.sequence_tokens.shape[0])
    pt.write(data, str(ptt))

    pt.compute_and_store_distances(str(ptt))                       # dense
    nnz = pt.compute_and_store_distances_sparse(str(ptt), cutoff=cutoff)  # sparse

    dense_kb = round(_dir_bytes(ptt / "pairs" / "distance_matrix") / 1024, 1)
    sparse_kb = round(_dir_bytes(ptt / "pairs_sparse" / "distance_matrix") / 1024, 1)

    # fidelity: rehydrated sparse == dense within the cutoff
    full = compute_distance_matrix(data.backbone_positions)
    dense_rehydrated = pt.read_pair_feature_sparse(str(ptt), "distance_matrix").to_dense()[:, :, 0]
    keep = full <= cutoff
    lossless = bool(np.allclose(dense_rehydrated[keep], full[keep], atol=1e-4)
                    and (dense_rehydrated[~keep] == 0).all())

    dense_read = _median_ms(lambda: pt.read_pair_feature(str(ptt), "distance_matrix"), rounds)
    sparse_read = _median_ms(lambda: pt.read_pair_feature_sparse(str(ptt), "distance_matrix"), rounds)

    import shutil; shutil.rmtree(tmp, ignore_errors=True)
    return {
        "id": pid, "n_res": n, "cutoff": cutoff,
        "nnz": nnz, "density_pct": round(100.0 * nnz / (n * n), 2),
        "dense_kb": dense_kb, "sparse_kb": sparse_kb,
        "disk_reduction": round(dense_kb / sparse_kb, 1) if sparse_kb else None,
        "dense_read_ms": dense_read, "sparse_read_ms": sparse_read,
        "roundtrip_lossless": lossless,
    }


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--cutoff", type=float, default=15.0)
    ap.add_argument("--rounds", type=int, default=7)
    args = ap.parse_args()

    import proteintensor as pt
    print(f"Sparse vs dense pair benchmark, cutoff {args.cutoff} A, {args.rounds} rounds ...")
    rows = []
    for pid in STRUCTS:
        print(f"  - {pid}")
        r = bench(pid, args.cutoff, args.rounds, pt)
        if r:
            rows.append(r)
    if not rows:
        print("No structures available."); return

    import platform, sys, zarr
    env = {
        "timestamp_utc": datetime.now(timezone.utc).isoformat(timespec="seconds"),
        "python": sys.version.split()[0], "platform": platform.platform(),
        "numpy": np.__version__, "zarr": zarr.__version__,
        "proteintensor": getattr(pt, "__version__", "unknown"),
        "cutoff_angstrom": args.cutoff,
    }
    record = {"env": env, "cases": rows}
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    stamp = env["timestamp_utc"].replace(":", "").replace("-", "").replace("+0000", "Z")
    (RESULTS_DIR / f"sparse_{stamp}.json").write_text(json.dumps(record, indent=2))

    lines = [
        "# Sparse vs dense pair-feature benchmark", "",
        f"Ca-Ca distance matrix stored dense (`pairs/`) vs sparse radius graph "
        f"(`pairs_sparse/`, cutoff {args.cutoff} A). Generated by "
        "`python benchmarks/sparse_pairs_benchmark.py`.", "",
        f"- **Platform:** {env['platform']} | proteintensor {env['proteintensor']} "
        f"| numpy {env['numpy']} | zarr {env['zarr']}",
        f"- **Run:** {env['timestamp_utc']}", "",
        "| Structure | Res | kept | density | dense KB | sparse KB | disk reduction | "
        "dense read | sparse read | lossless |",
        "|---|---|---|---|---|---|---|---|---|---|",
    ]
    for r in rows:
        lines.append(
            f"| {r['id']} | {r['n_res']} | {r['nnz']:,} | {r['density_pct']}% | "
            f"{r['dense_kb']} | {r['sparse_kb']} | **{r['disk_reduction']}x** | "
            f"{r['dense_read_ms']} ms | {r['sparse_read_ms']} ms | "
            f"{'yes' if r['roundtrip_lossless'] else 'NO'} |")
    lines += ["", "> Radius graph is lossless *within the cutoff*; pairs beyond the cutoff",
              "> are intentionally dropped (implicitly zero). Choose the cutoff for the",
              "> receptive field your model needs.", ""]
    (Path(__file__).resolve().parent / "SPARSE_PAIRS_RESULTS.md").write_text("\n".join(lines) + "\n")

    print(f"\nWrote SPARSE_PAIRS_RESULTS.md")
    for r in rows:
        print(f"  {r['id']:6s} {r['n_res']:5d} res  dense {r['dense_kb']:9.1f}KB  "
              f"sparse {r['sparse_kb']:8.1f}KB  ->  {r['disk_reduction']}x  "
              f"(density {r['density_pct']}%)")


if __name__ == "__main__":
    main()
